
numslicesbread = 20
numslicescheese = 7

while((numslicesbread > 1) and (numslicescheese > 0)):
    print("Making grilled cheese!")
    numslicesbread -= 2
    numslicescheese -= 1

print("All done")
print("Remaining bread: ", numslicesbread, "slices")
print("Remaining cheese: ", numslicescheese, "slices")
